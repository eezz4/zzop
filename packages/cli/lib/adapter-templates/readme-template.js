'use strict';

// Builds the scaffolded adapter's own README.md for `zzop init adapter --mode <a|b> --kind <consume|provide>`.

const MODE_BLURB = {
  a: [
    'Mode A — full envelope. `main.mjs` replaces native analysis for this tree: it must describe every',
    'file it sees (symbols/imports/io), not just files that carry IO. Feed its output to',
    '`analyzeEnvelope(envelopeJson, configJson)` if embedding `@zzop/native` directly.',
  ],
  b: [
    'Mode B — io-only overlay. `main.mjs` emits a partial envelope (usually only `io`, plus `markEntry`',
    'for framework-loaded files) that the engine merges on top of native analysis for this tree. Attach',
    'it via the `overlays` config key in `zzop.config.jsonc`:',
    '',
    '```jsonc',
    '{',
    '  "roots": ["."],',
    '  "overlays": ["./zzop-adapter/envelope.json"]',
    '}',
    '```',
  ],
};

const KIND_BLURB = {
  consume: 'This scaffold is wired for the **consume** side: outbound call sites (`addConsume`).',
  provide: 'This scaffold is wired for the **provide** side: route/endpoint definitions (`addProvide`).',
};

/**
 * @param {{ mode: 'a'|'b', kind: 'consume'|'provide' }} opts
 * @returns {string}
 */
function buildReadmeTemplate({ mode, kind }) {
  const lines = [
    '# zzop adapter (generated scaffold)',
    '',
    `Generated by \`zzop init adapter --mode ${mode} --kind ${kind}\`.`,
    '',
    'This directory is self-contained: `lib/keys.mjs` and `lib/envelope.mjs` are bundled copies of',
    '[`examples/adapter-kit`](https://github.com/eezz4/zzop/tree/main/examples/adapter-kit)\'s helpers —',
    'that kit is not published to npm, so this scaffold carries its own copies rather than depending on',
    'it. See each bundled file\'s header comment for its upstream source of truth.',
    '',
    '## What to fill in',
    '',
    '`main.mjs` has `TODO(vocabulary)` markers at every spot that needs framework-specific extraction',
    'logic (file walking is already wired; you write the "find the routes/call-sites in this file\'s',
    'text" part). Run it with:',
    '',
    '```sh',
    'node main.mjs <path-to-tree-root> > envelope.json',
    '```',
    '',
    ...MODE_BLURB[mode],
    '',
    KIND_BLURB[kind],
    '',
    '## Validate before wiring it in',
    '',
    'Check the envelope offline (structural validation against the v1 contract, plus lint hints) before',
    'attaching it to a real run:',
    '',
    '```sh',
    'zzop adapter validate envelope.json',
    '```',
    '',
    '## Keeping the key normalization correct',
    '',
    'Cross-layer linking is an EXACT string join on `key` — a key computed even slightly differently',
    "than the engine's own normalization silently fails to join (the consume just lands in",
    '`unprovidedConsumes` instead of forming an edge). If you modify `lib/keys.mjs`, replay',
    '[`docs/adapters/key-normalization.fixture.json`](https://github.com/eezz4/zzop/blob/main/docs/adapters/key-normalization.fixture.json)\'s',
    "rows against your version in your own tests before trusting it (mirror adapter-kit's own",
    '`test/keys.test.js`).',
    '',
    '## Further reading',
    '',
    '- [`docs/adapters/README.md`](https://github.com/eezz4/zzop/blob/main/docs/adapters/README.md) —',
    '  the full envelope contract, versioning policy, and the adapter-kit this scaffold was copied from.',
    '- [`docs/NORMALIZED_AST.md`](https://github.com/eezz4/zzop/blob/main/docs/NORMALIZED_AST.md) — the',
    '  `NormalizedEnvelope`/`FileProjection` field reference.',
    '',
  ];
  return lines.join('\n');
}

module.exports = { buildReadmeTemplate };
