'use strict';

// Builds the scaffolded `main.mjs` entry point for `zzop init adapter --mode <a|b> --kind <consume|provide>`.
// Built as an array of lines (not one giant template literal) so the generated source's own template
// literals/backticks never collide with this builder's own string syntax.

const MODE_LABELS = { a: 'A (full envelope)', b: 'B (io-only overlay)' };
const KIND_LABELS = { consume: 'consume', provide: 'provide' };

const WALK_HELPER = [
  '// Minimal recursive file walk (deterministic — lexically sorted) — NOT bundled from adapter-kit;',
  '// this scaffold only bundles the join-critical pieces (lib/keys.mjs, lib/envelope.mjs). Replace or',
  '// extend freely (extension filter, skip-dir list, ...).',
  'function walk(root, extensions) {',
  '  const out = [];',
  '  (function walkDir(dir) {',
  '    for (const entry of readdirSync(dir, { withFileTypes: true })) {',
  "      if (entry.name === 'node_modules' || entry.name === '.git') continue;",
  '      const full = path.join(dir, entry.name);',
  '      if (entry.isDirectory()) {',
  '        walkDir(full);',
  '        continue;',
  '      }',
  '      if (!entry.isFile()) continue;',
  '      if (extensions && !extensions.includes(path.extname(entry.name))) continue;',
  "      out.push(path.relative(root, full).split(path.sep).join('/'));",
  '    }',
  '  })(root);',
  '  out.sort();',
  '  return out;',
  '}',
].join('\n');

// `callLine` is the exact comment line showing the add call — mode B prepends an `ensureFile();` line
// above it (registers the file lazily, only once IO is actually found); mode A never needs it (every
// walked file is already registered via addFile above the extraction TODO).
function consumeExtractionTodo({ withEnsureFile }) {
  const lines = [
    '  // TODO(vocabulary): find outbound call sites in `text` (framework-specific: fetch(...),',
    '  // axios.get(...), your own SDK/client wrapper, ...). For each one, extract (method, url, line)',
    '  // and resolve the key with the bundled helper:',
    '  //',
    '  //   const key = resolveConsumeKey(method, url); // null when unresolvable -- never guessed',
  ];
  if (withEnsureFile) lines.push('  //   ensureFile();');
  lines.push(
    "  //   builder.addConsume(rel, {",
    "  //     kind: 'http',",
    '  //     key,',
    '  //     line,',
    '  //     raw: key === null ? url : undefined,',
    '  //     method: key === null ? method : undefined, // set only alongside a null key',
    '  //   });'
  );
  return lines.join('\n');
}

function provideExtractionTodo({ withEnsureFile }) {
  const lines = [
    '  // TODO(vocabulary): find route/endpoint DEFINITIONS in `text` (framework-specific:',
    '  // app.get(...), @Get() decorators, file-based routing, ...). For each one, extract',
    '  // (method, routePath, line, handlerName) and key it with the bundled helper:',
    '  //',
    '  //   const key = normalizeProvideKey(method, routePath);',
  ];
  if (withEnsureFile) lines.push('  //   ensureFile();');
  lines.push("  //   builder.addProvide(rel, { kind: 'http', key, line, symbol: handlerName });");
  return lines.join('\n');
}

/**
 * @param {{ mode: 'a'|'b', kind: 'consume'|'provide' }} opts
 * @returns {string}  full `main.mjs` source
 */
function buildMainTemplate({ mode, kind }) {
  const keyImport = kind === 'consume' ? 'resolveConsumeKey' : 'normalizeProvideKey';
  const withEnsureFile = mode === 'b';
  const extractionTodo =
    kind === 'consume' ? consumeExtractionTodo({ withEnsureFile }) : provideExtractionTodo({ withEnsureFile });

  const lines = [];
  lines.push(
    `// zzop adapter scaffold — Mode ${MODE_LABELS[mode]}, ${KIND_LABELS[kind]}-side.`,
    '//',
    `// Generated by \`zzop init adapter --mode ${mode} --kind ${kind}\`. Self-contained: lib/keys.mjs`,
    "// and lib/envelope.mjs are bundled copies of examples/adapter-kit's helpers (that kit is not",
    '// published to npm — see docs/adapters/README.md). Fill in the TODO(vocabulary) spots below, then:',
    '//'
  );
  if (mode === 'a') {
    lines.push(
      '//   node main.mjs <root> > envelope.json',
      '//',
      '// Mode A is a FULL envelope — it REPLACES native analysis for this tree. Feed it to',
      "// `analyzeEnvelope(envelopeJson, configJson)` if you're embedding @zzop/native directly, or run",
      '// `zzop adapter validate envelope.json` first to check it offline.'
    );
  } else {
    lines.push(
      '//   node main.mjs <root> > envelope.json',
      '//',
      '// Mode B is an io-only OVERLAY — the engine merges it on top of native analysis for this tree.',
      '// Attach it via the `overlays` config key in zzop.config.jsonc (top-level, or per-tree as',
      '// `trees[i].overlays`):',
      '//',
      '//   "overlays": ["./zzop-adapter/envelope.json"]',
      '//',
      '// Run `zzop adapter validate envelope.json` first to check it offline.'
    );
  }
  lines.push('', "import { readFileSync, readdirSync } from 'node:fs';", "import path from 'node:path';");
  lines.push("import { EnvelopeBuilder } from './lib/envelope.mjs';");
  lines.push(`import { ${keyImport} } from './lib/keys.mjs';`);
  lines.push('');
  lines.push(
    "// TODO(vocabulary): a stable id for this adapter + its output shape, e.g. 'my-router-adapter/1'.",
    "const PARSER_ID = 'TODO-adapter-id/1';",
    '// TODO(vocabulary): the tree/source id this envelope is attributed to — must match the sourceId',
    '// (or root) of the tree this overlay/envelope is analyzed against.',
    "const SOURCE_ID = 'TODO-source-id';",
    ''
  );
  lines.push(WALK_HELPER, '');
  lines.push(
    'const root = process.argv[2];',
    'if (!root) {',
    "  process.stderr.write('usage: node main.mjs <root>\\n');",
    '  process.exit(2);',
    '}',
    ''
  );
  lines.push('const builder = new EnvelopeBuilder({ parser: PARSER_ID, source: SOURCE_ID });', '');
  lines.push(
    "// TODO(vocabulary): your source extensions (e.g. ['.ts', '.tsx']) — walk() returns every matching",
    '// file, deterministically sorted.',
    "for (const rel of walk(root, ['.ts', '.tsx', '.js', '.jsx'])) {",
    "  const text = readFileSync(path.join(root, rel), 'utf8');",
    ''
  );
  if (mode === 'a') {
    lines.push(
      '  // Mode A = FULL envelope: describe every file you see, not just files with IO.',
      "  // TODO(vocabulary): extract this file's symbols/imports/re_exports and pass them as addFile's",
      "  // opts (see docs/NORMALIZED_AST.md's FileProjection) — left as loc-only below as a starting point.",
      "  builder.addFile(rel, { loc: text.split('\\n').length });",
      ''
    );
  } else {
    lines.push(
      '  // Mode B = io-only OVERLAY: only register a file once it actually carries IO (or is a',
      '  // framework-loaded entrypoint via markEntry) — leave symbols/imports empty, the native parser',
      "  // already covers this file's structure. `registered` guards addFile's \"each file once\" rule.",
      '  let registered = false;',
      '  const ensureFile = () => {',
      '    if (!registered) {',
      '      builder.addFile(rel);',
      '      registered = true;',
      '    }',
      '  };',
      ''
    );
  }
  lines.push(extractionTodo, '');
  lines.push('}', '');
  lines.push('process.stdout.write(JSON.stringify(builder.toEnvelope()));', '');
  return lines.join('\n');
}

module.exports = { buildMainTemplate };
